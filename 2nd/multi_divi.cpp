#include <iostream>

int main() {
    int num1 = 12;
    int num2 = 4;

    int multiplication = num1 * num2;
    int division = num1 / num2;

    std::cout << "Multiplication of " << num1 << " and " << num2 << " is: " << multiplication << std::endl;
    std::cout << "Division of " << num1 << " by " << num2 << " is: " << division << std::endl;

    return 0;
}
