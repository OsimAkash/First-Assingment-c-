#include <iostream>
#include <string>

int main() {
    // Declare and initialize variables
    int height = 170;          // Height in centimeters
    double weight = 65.5;      // Weight in kilograms
    std::string eyeColor = "brown"; // Eye color

    // Print the variables
    std::cout << "Height: " << height << " cm" << std::endl;
    std::cout << "Weight: " << weight << " kg" << std::endl;
    std::cout << "Eye Color: " << eyeColor << std::endl;

    return 0;
}
