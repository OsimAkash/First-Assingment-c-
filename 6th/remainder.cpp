#include <iostream>

int main() {
    // Declare and initialize variables
    int dividend = 15;
    int divisor = 4;

    // Calculate the remainder
    int remainder = dividend % divisor;

    // Print the remainder
    std::cout << "The remainder of dividing " << dividend << " by " << divisor << " is: " << remainder << std::endl;

    return 0;
}
