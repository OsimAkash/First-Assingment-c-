#include <iostream>

int main() {
    // Declare and initialize two integer variables
    int num1 = 5;  // First number
    int num2 = 10; // Second number

    // Add the two numbers
    int sum = num1 + num2; // Sum of num1 and num2

    // Print the result
    std::cout << "The sum of " << num1 << " and " << num2 << " is: " << sum << std::endl;

    return 0;
}
